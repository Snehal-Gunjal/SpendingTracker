    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
          String category = jTextField1.getText();
          if(!category.equals(""))
          {
            DB.DB_Connect.st.executeUpdate("insert into category_info values('"+category+"')");
            JOptionPane.showMessageDialog(null,"Category Added Successfully");
            getEntries();  
          }
          else
          {
            JOptionPane.showMessageDialog(null,"Please Enter Category!");  
          }
        }
        catch(SQLIntegrityConstraintViolationException ex)
        {
            JOptionPane.showMessageDialog(null,"Category Already Exist");
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ri = jTable1.getSelectedRow();
        
        if(ri!=-1)
        {
            int r = JOptionPane.showConfirmDialog(null,"Do You Really Want To Delete?","Confirmation Window",JOptionPane.YES_NO_OPTION);

            if(r==JOptionPane.YES_OPTION)
            {
                String Category = (String)jTable1.getValueAt(ri,1);

                try
                {
                    DB.DB_Connect.st.executeUpdate("delete from category_info where category='"+Category+"'");
                    JOptionPane.showMessageDialog(null,"Category Deleted Successfully!");
                    getEntries();
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null,ex);
                } 
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

