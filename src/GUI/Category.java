private void getEntries()
    {
       try
       {
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable1.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0); //To make table empty
            }
            
            int sno = 0;
            ResultSet rs = DB.DB_Connect.st.executeQuery("select * from category_info");
            
            while(rs.next())
            {
               String category = rs.getString("category"); //To dat data in table
               Object ob[] = {++sno,category}; 
               dtm.addRow(ob);
            }
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null,ex);
       }
    }

