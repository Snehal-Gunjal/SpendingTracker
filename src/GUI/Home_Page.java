    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int ri = jTable1.getSelectedRow();
        
        if(ri!=-1)
        {
            int r = JOptionPane.showConfirmDialog(null,"Do You Really Want To Delete?","Confirmation Window",JOptionPane.YES_NO_OPTION);

            if(r==JOptionPane.YES_OPTION)
            {
                int id = (int)jTable1.getValueAt(ri, 0);
                try
                {
                     DB.DB_Connect.st.executeUpdate("Delete from spendings where s_id='"+id+"' ");
                     JOptionPane.showMessageDialog(null,"Seccessfully Deleted!");
                     getEntries();
                }
                catch(Exception ex)
                {
                    JOptionPane.showMessageDialog(null,ex);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

