    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {
           java.util.Date dt = jDateChooser1.getDate(); 
            String s1 = jTextField1.getText() ;
            String s2 = (String)jComboBox1.getSelectedItem();
            
            if(dt!=null && !s1.equals("") && !s2.equals(""))
            {
                int amount = Integer.parseInt(s1);
                java.sql.Date date = new java.sql.Date(dt.getTime());
                DB.DB_Connect.st.executeUpdate("insert into spendings (category,s_date,amount) values ('"+s2+"','"+date+"',"+amount+")");
                JOptionPane.showMessageDialog(null,"Expence Added Successfully!");
                getEntries();
            
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please Fill All Details");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

