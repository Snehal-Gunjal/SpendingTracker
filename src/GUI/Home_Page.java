private void getEntries()
{
    try
    {
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable1.getModel();
        int rc = dtm.getRowCount();
        
        while(rc--!=0)
        {
            dtm.removeRow(0);
        }
        java.time.LocalDate cd=java.time.LocalDate.now();
        java.time.LocalDate bd=cd.minusDays(20);
        
        ResultSet rs = DB.DB_Connect.st.executeQuery("Select * from spendings where s_date <='"+cd+"' and s_date>='"+bd+"' ");
        int total = 0 ;
        while(rs.next())
        {
            int t = rs.getInt("amount"); 
            total+= t;
            Object o[] = {rs.getInt("s_id"),rs.getString("category"),rs.getDate("s_date"),t}; 
            dtm.addRow(o);
        }
        jLabel6.setText(total+"");
    }
    catch(Exception ex)
    {
        JOptionPane.showMessageDialog(null,ex);
    }
}
private void display_Category()
{
    try
    {
       jComboBox1.removeAllItems();
       ResultSet rs = DB.DB_Connect.st.executeQuery("select * from category_info");
       while(rs.next())
       {
           jComboBox1.addItem(rs.getString("category"));
       }
    }
    catch(Exception ex)
    {
        JOptionPane.showMessageDialog(null,ex);
    }   
}
